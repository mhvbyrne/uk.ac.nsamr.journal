        <div class="left-heading">Open access only</div>
        <div>
            <p> The journal provides open access to all its content and article submission is free for all authors.</p>
            <p> Why is JNSAMR online only? </p>
            <p> An online journal allows more immersive multimedia to be included, such as videos, interactive media, and all colour images and graphs.</p>
            <p> JNSAMR publishes all articles immediately following editing. Articles from the previous six months are then brought together to form a biannual issue.</P>
